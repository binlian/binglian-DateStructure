package com.binglian.UnionFind;

/**
 * 第四个版本 Quick Union 基于的size优化
 * rank[i]表示根节点为i的树的高度
 * @author binglian
 *
 */
public class UnionFind4 implements UF{

	private int[] parent;
	private int[] rank;	//rank[i]表示以i为根的集合所表示的树的层数
	
	
	
	public UnionFind4(int size){
		
		parent=new int[size];
		
		for(int i=0;i<size;i++){
			parent[i]=i;
			rank[i]=1;
		}
	}
	
	@Override
	public int getSize(){
		return parent.length;
	}

	
	
	/**
	 * 查找过程，查找元素p所对应的集合编号
	 * O(h)复杂度，h为树的高度
	 * @param p
	 * @return
	 */
	private int find(int p){
		
		if(p < 0 && p>=parent.length)
			throw new IllegalArgumentException("下标不合法");
		
		
		while(p !=parent[p])
			p=parent[p];
		return p;
	}

	
	//查找元素p和元素q是否所属一个集合
	@Override
	public boolean isConnected(int p,int q){
		return find(p) == find(q);
	}
	
	/**
	 * 合并元素p和元素q所属的集合
	 * O(h)复杂度，h为树的高度
	 */
	@Override
	public void unionElements(int p,int q){
		int pRoot=find(p);
		int qRoot=find(q);
		
		if(pRoot == qRoot)
			return ;
		
		//根据两个元素所在树的元素个数不同判断合并方向
		//将元素个数少的集合合并到元素个数多的集合上
		if(rank[pRoot] < rank[qRoot])//如果pRoot元素高度小于 就指向高的元素 pRoot=qRoot
			parent[pRoot] = qRoot;//pRoot 指向qRoot
		else if(rank[qRoot] < rank[pRoot])//反之亦然 q小于p qRoot就指向pRoot parent[qRoot]=parent[pRoot]
			parent[qRoot]=pRoot;//qRoot 指向pRoot
		else{//rank[qRoot] == rank[pRoot]
			parent[qRoot]=pRoot;
			rank[pRoot]+=1;//高度加1
		}
	}
}
